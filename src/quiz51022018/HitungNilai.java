       String nim=jTextField1.getText();
       String nama=jTextField2.getText();
       double ntugas=Double.parseDouble(jTextField3.getText());
       double nquis=Double.parseDouble(jTextField4.getText());
       double nmid=Double.parseDouble(jTextField5.getText());
       double nfinal=Double.parseDouble(jTextField7.getText());
       double nakhir=(0.2*ntugas)+(0.2*nquis)+(0.2*nmid)+(0.2*nfinal);
       jTextField8.setText(String.valueOf(nakhir));
       
       char nhuruf;
       String ket;
       if(nakhir >=80) {
           nhuruf='A';
           ket="Selamat atas kelulusan anda";
       }
       else if(nakhir >=70) {
           nhuruf='B';
           ket="Selamat atas kelulusan anda";
       }
       else if(nakhir >=60) {
           nhuruf='C';
           ket="Selamat atas kelulusan anda";
       }
       else if(nakhir >=50) {
           nhuruf='D';
           ket="Mohon maaf anda tidak lulus";
       }
       else {
           nhuruf='E';
           ket="Mohon maaf anda tidak lulus";
       }
       jTextField9.setText(String.valueOf(nhuruf));
       jTextField6.setText(ket);
